/** Currently active language */
let currentLang = 'en';

/** Store original English text for toggle-back */
const enCache = new Map();

/**
 * Apply a language to all [data-i18n] elements on the page.
 * @param {'en'|'hi'} lang
 */
export function setLanguage(lang) {
  currentLang = lang;
  document.documentElement.lang = lang;
  localStorage.setItem('grosint-lang', lang);

  const els = document.querySelectorAll('[data-i18n], [data-i18n-html]');
  els.forEach(el => {
    const key = el.getAttribute('data-i18n') || el.getAttribute('data-i18n-html');
    if (!key) return;

    const useHTML = el.hasAttribute('data-i18n-html');

    // Cache original English on first encounter
    if (!enCache.has(el)) {
      enCache.set(el, useHTML ? el.innerHTML : el.textContent);
    }

    if (lang === 'hi' && HI[key]) {
      if (useHTML) {
        el.innerHTML = HI[key];
      } else {
        el.textContent = HI[key];
      }
    } else {
      // Restore English
      const original = enCache.get(el);
      if (original !== undefined) {
        if (useHTML) {
          el.innerHTML = original;
        } else {
          el.textContent = original;
        }
      }
    }
  });

  // Update toggle button label
  const toggleBtn = document.getElementById('lang-toggle');
  if (toggleBtn) {
    toggleBtn.innerHTML = lang === 'en'
      ? '<span class="lang-toggle__active">EN</span> | हिं'
      : 'EN | <span class="lang-toggle__active">हिं</span>';
  }
}

/** Initialize: check localStorage and set up toggle button */
export function initI18n() {
  const saved = localStorage.getItem('grosint-lang');
  const toggleBtn = document.getElementById('lang-toggle');

  if (toggleBtn) {
    toggleBtn.addEventListener('click', () => {
      setLanguage(currentLang === 'en' ? 'hi' : 'en');
    });
  }

  if (saved && saved !== 'en') {
    setLanguage(saved);
  }
}
